context_free_parse::LR1ParseRule getLR1Rule(unsigned state, common::Kind sym)
{
    context_free_parse::LR1ParseRule lr1Rule;
    unsigned i = 0;
    for (; i < LR1_RULES_SIZE; ++i) {
        if (state == LR1_RULES[i].state &&
            sym == LR1_RULES[i].sym) {
            lr1Rule = LR1_RULES[i];
            break;
        }
    }
    if (i == LR1_RULES_SIZE) {
        throw new error::ErrorObject(error::NO_MATCHING_PROD_RULE,
            "lr1_rules::getLR1Rule(unsigned, common::Kind)",
            "No rule found!");
    }
    return lr1Rule;
}

