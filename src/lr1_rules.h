{
    context_free_parse::LR1ParseRule lr1Rule;
    unsigned i = 0;
    for (; i < LR1_RULES_SIZE; ++i) {
        if (state == LR1_RULES[i].state &&
            sym == LR1_RULES[i].sym) {
            lr1Rule = LR1_RULES[i];
            break;
        }
    }
    if (i == LR1_RULES_SIZE) assert("lr1_rules::getLR1Rule - No rule found!\n");
    return lr1Rule;
}

